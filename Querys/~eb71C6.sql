select C.EXTERNAL_ID,
       A.ACCOUNT_NO,
       A.BILL_REF_NO,
       trunc(B.CREATION_DATE) CREATION_DATE,
       A.FILE_NAME,
       B.NO_BYTES,
       C.VERSION_FEED,
       A.FORMAT_STATUS
  from (select ACCOUNT_NO, BILL_REF_NO, FILE_NAME, FORMAT_STATUS from GVT_FBB_BILL_INVOICE
        union all
        select ACCOUNT_NO, BILL_REF_NO, FILE_NAME, FORMAT_STATUS from GVT_FEBRABAN_BILL_INVOICE) A,
       GVT_FEBRABAN_BILL_FILES B,
       GVT_FEBRABAN_ACCOUNTS C
 where A.ACCOUNT_NO = C.ACCOUNT_NO
   and A.FILE_NAME = B.FILENAME
   and a.bill_ref_no in (321776428,321776421,321774438,321775583,321774579,321778822,321776380,321778821,321778820,321778819,321778816,321776548,321773764,321775537,321773747,321783146,321773739,321777613,321777612,321778814,321778813,321778812,321778811,321778810,321778808,321778805,321777014,321778802,321778801,321778795,321778794,321778793,321778524,321774120,321783145,321786897,323552505,323553905)
   --and C.EXTERNAL_ID in ('999979696307')
   -- and C.ACCOUNT_NO = 3847517
 order by 4 desc;


