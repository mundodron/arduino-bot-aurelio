select S.FULL_SIN_SEQ FULL_SIN_SEQ_ssn,
      S.BILL_REF_NO bill_ref_no_ssn,
      S.OPEN_ITEM_ID OPEN_ITEM_ID_ssn,
      C.BILL_REF_NO bill_ref_no_cbd,
      C.OPEN_ITEM_ID  OPEN_ITEM_ID_cbd ,
      c.external_id
from sin_seq_no s, 
    cmf_balance_detail c
where C.BILL_REF_NO = S.BILL_REF_NO (+)
and c.bill_ref_no in (97975000,97956002,97968003,98002005,98000006,97961006,97965006,97970011,98013011,97968012,97948013,97991013,98006015,97945016,97978017,98010017)
and C.OPEN_ITEM_ID not in (2,3)
and S.OPEN_ITEM_ID(+) = decode(C.OPEN_ITEM_ID,1,0,C.OPEN_ITEM_ID)
and c.BILL_REF_NO not in (97948013,97965006,97968003,97968012,97978017,97991013,98002005,98006015,98010017,98013011)


