select C.EXTERNAL_ID,
       A.ACCOUNT_NO,
       A.BILL_REF_NO,
       B.CREATION_DATE,
       A.FILE_NAME,
       B.NO_BYTES,
       C.VERSION_FEED,
       A.FORMAT_STATUS
  from (select ACCOUNT_NO, BILL_REF_NO, FILE_NAME, FORMAT_STATUS from GVT_FBB_BILL_INVOICE
        union all
        select ACCOUNT_NO, BILL_REF_NO, FILE_NAME, FORMAT_STATUS from GVT_FEBRABAN_BILL_INVOICE) A,
       GVT_FEBRABAN_BILL_FILES B,
       GVT_FEBRABAN_ACCOUNTS C
 where A.ACCOUNT_NO = C.ACCOUNT_NO
   and A.FILE_NAME = B.FILENAME
   and a.bill_ref_no in (280946107 ) --(272794119,272795318,272796716,272798728,272789110,272790729,272790734,272791327,272793003,272793127,272793334,272793335,272793336,272793343,272793349,272793352,272793353,272797234,272799326,273849906,273850904)
  -- and C.EXTERNAL_ID in ('999979882050')
   -- and C.ACCOUNT_NO = 3847517
 order by 4 desc;

